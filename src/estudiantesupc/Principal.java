    private void pintarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        String[] columnas = {"CODIGO", "NOMBRE", "SISBEN", "PROMEDIO", "EDAD", "BENEFICIADO"};
        modelo.setColumnIdentifiers(columnas);
        for (Estudiante p : listaEstudiante) {
            String[] fila = {String.valueOf(p.getCodigo()), p.getNombre(), String.valueOf(p.getSisben()), String.valueOf(p.getPromedio()), String.valueOf(p.getEdad())};
            modelo.addRow(fila);
        }
        estudiantesTabla.setModel(modelo);
    }

