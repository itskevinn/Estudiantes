    public int posicionEstudiante = -1;
    public static double valorMatricula = 900000;

    private void asignarMatriculaYValidar() {        
        int beneficiario = 0;        
        if (posicionEstudiante != -1) {
            for (Estudiante estudiante : listaEstudiante) {
                if (estudiante.getPromedio() >= 4) {
                    estudiante.setBeneficiado("BENEFICIARIO");
                    estudiante.setMatricula(valorMatricula - (valorMatricula * 0.7));
                    listaEstudiante.add(estudiante);
                    listaEstudiantesAptos.add(estudiante);
                    beneficiario = 1;
                    Archivos.escribirBinario(listaEstudiantesAptos);
                    break;
                } else if (estudiante.getPromedio() >= 3.5 && 30 > estudiante.getSisben()) {
                    estudiante.setMatricula(valorMatricula - (valorMatricula * 0.6));
                    estudiante.setBeneficiado("BENEFICIARIO");
                    listaEstudiante.add(estudiante);
                    listaEstudiantesAptos.add(estudiante);
                    beneficiario = 1;
                    Archivos.escribirBinario(listaEstudiantesAptos);
                    break;
                } else if (estudiante.getPromedio() >= 3.2 && 20 > estudiante.getSisben()) {
                    estudiante.setMatricula(valorMatricula - (valorMatricula * 0.4));
                    estudiante.setBeneficiado("BENEFICIARIO");
                    listaEstudiante.add(estudiante);
                    listaEstudiantesAptos.add(estudiante);
                    beneficiario = 1;
                    Archivos.escribirBinario(listaEstudiantesAptos);
                    break;
                } else if (estudiante.getPromedio() >= 3.0 && 18 > estudiante.getEdad()) {
                    estudiante.setMatricula(valorMatricula - (valorMatricula * 0.2));
                    estudiante.setBeneficiado("BENEFICIARIO");
                    listaEstudiante.add(estudiante);
                    listaEstudiantesAptos.add(estudiante);
                    beneficiario = 1;
                    Archivos.escribirBinario(listaEstudiantesAptos);
                    break;
                }
                if (20 > estudiante.getEdad()) {
                    estudiante.setMatricula(valorMatricula - (valorMatricula * 0.1));
                    estudiante.setBeneficiado("BENEFICIARIO");
                    listaEstudiantesAptos.add(estudiante);
                    listaEstudiante.add(estudiante);
                    Archivos.escribirBinario(listaEstudiantesAptos);
                    beneficiario = 1;
                    break;
                }
                if (beneficiario == 0) {
                    estudiantesNoBeneficiados.add(estudiante);
                    listaEstudiante.add(estudiante);
                }
            }
        }
    }

    public int encontrar() {
        int encontrado = 0;
        for (Estudiante estudiante : listaEstudiante) {
            if (identificacionABuscar.getText().equals(estudiante.getCodigo())) {
                encontrado = 1;
                estudianteEncontrado = estudiante;
            }
        }
        return encontrado;
    }
